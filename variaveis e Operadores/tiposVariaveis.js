// o var é uma variavel de escopo global, não possui escopo de bloco, não é recomendado o seu uso.
var a = 'escopo global'
var num1 = 15
var verdadeiro = true

// o let tem escopo de bloco, o que significa que ela é acessível apenas dentro do bloco onde é declarada.
let b = 'escopo de bloco'
let num2 = 15
let falso = false


// o const é uma constante, ou seja, seu valor não pode ser alterado depois de atribuído
const c = 'valor fixo'
const num3 = 15
const booleano = true